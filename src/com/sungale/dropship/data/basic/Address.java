public class Address {
	private String firstName;
	private String lastName;
	private String businessName;
	private String streetAddress;
	private String roomFloorAddress;
	private String city;
	private String state;
	private String country;
	private String zip;
	private String phone;
	private boolean isResidence;
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getBusinessName() {
		return businessName;
	}
	public void setBusinessName(String businessName) {
		this.businessName = businessName;
	}
	public String getStreetAddress() {
		return streetAddress;
	}
	public void setStreetAddress(String streetAddress) {
		this.streetAddress = streetAddress;
	}
	public String getRoomFloorAddress() {
		return roomFloorAddress;
	}
	public void setRoomFloorAddress(String roomFloorAddress) {
		this.roomFloorAddress = roomFloorAddress;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public boolean isResidence() {
		return isResidence;
	}
	public void setResidence(boolean isResidence) {
		this.isResidence = isResidence;
	}

	

}
